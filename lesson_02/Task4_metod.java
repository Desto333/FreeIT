package lesson_02;

import java.util.Scanner;

public class Task4_metod {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int num = scanner.nextInt();
        int fstDigit = num%10*1000;
        int secDigit = (num%100 - num%10)*10;
        int thdDigit = (num%1000 - num%100)/10;
        int frthDigit = (num - num%1000)/1000;
        int finalNum = fstDigit + secDigit + thdDigit + frthDigit;

        System.out.println(finalNum);
    }
}
