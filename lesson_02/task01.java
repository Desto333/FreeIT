import java.util.Scanner;

public class Task01 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String number = scanner.nextLine();
        if(Integer.parseInt(number) > 0 && number.length() == 1) System.out.println("Это однозначное положительное число");
        if(Integer.parseInt(number) > 0 && number.length() == 2) System.out.println("Это двузначное положительное число");
        if(Integer.parseInt(number) > 0 && number.length() >= 3) System.out.println("Это трехзначное или более положительное число");
        if(Integer.parseInt(number) < 0 && number.length() == 2) System.out.println("Это однозначное отрицательное число");
        if(Integer.parseInt(number) < 0 && number.length() == 3) System.out.println("Это двузначное отрицательное число");
        if(Integer.parseInt(number) < 0 && number.length() >= 4) System.out.println("Это трехзначное или более отрицательное число");
        if(Integer.parseInt(number) == 0) System.out.println("Это ноль");
    }
}
